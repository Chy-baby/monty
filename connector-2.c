#include "monty.h"
/**
 * queue -  adds data to the back of the queue
 * @stack: double linkedlist
 * @line_number: line num
 */

void queue(stack_t **stack, unsigned int line_number)
{
	(void)stack;
	(void)line_number;
	args.layout = 0;
}


/**
 * stack - adds data to top of stack
 * @stack: double linkedlist
 * @line_number: num line
 */

void stack(stack_t **stack, unsigned int line_number)
{
	(void)stack;
	(void)line_number;
	args.layout = 1;
}
